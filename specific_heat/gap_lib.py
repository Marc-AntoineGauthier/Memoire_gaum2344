import numpy as np
dpi = 2*np.pi

def zero(k): return 0

def un(k): return 1

def xy_mx2py2(k):
    xy = np.sin(dpi*k[0])*np.sin(dpi*k[1])
    mx2py2 = np.cos(dpi*k[0]) - np.cos(dpi*k[1])
    return xy*mx2py2

def xy(k):
    return np.sin(dpi*k[0])*np.sin(dpi*k[1])

def pzypizx(k):
    zy = np.sin(dpi*k[2])*np.sin(dpi*k[1])
    zx = np.sin(dpi*k[2])*np.sin(dpi*k[0])
    return zy + 1j*zx

def pzymizx(k):
    zy = np.sin(dpi*k[2])*np.sin(dpi*k[1])
    zx = np.sin(dpi*k[2])*np.sin(dpi*k[0])
    return zy - 1j*zx

def px2py2(k):
    return np.cos(dpi*k[0]) + np.cos(dpi*k[1])

def izypzx(k):
    zy = np.sin(dpi*k[2])*np.sin(dpi*k[1])
    zx = np.sin(dpi*k[2])*np.sin(dpi*k[0])
    return zx + 1j*zy

def mizypzx(k):
    zy = np.sin(dpi*k[2])*np.sin(dpi*k[1])
    zx = np.sin(dpi*k[2])*np.sin(dpi*k[0])
    return zx - 1j*zy

def px2my2(k):
    return np.cos(dpi*k[1]) - np.cos(dpi*k[0])

